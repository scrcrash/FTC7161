void driveDistance(int inches)
{
	//Reset encoder
	nMotorEncoder[motorTR] = 0;

	//Encoders have 1440 ticks per revolution of the wheel
	//Encoder ticks = (1440/circumference) * Distance to travel
	//Wheel diameter = 4 inches
	//Wheel circumference = 12.56 inches
	//1440 / 12.56 = 114.6 ticks per inch
	//Round 114.6 ticks per inch to 1146 ticks per 10 inches
	int tickGoal = (2866 * inches) / 1000;

	while(nMotorEncoder[motorTR] < tickGoal)
	{
		motor[motorTR] = 20;
		motor[motorTL] = 20;
		motor[motorBR] = 20;
		motor[motorBL] = 20;
	}

	motor[motorTR] = 0;
	motor[motorTL] = 0;
	motor[motorBR] = 0;
	motor[motorBL] = 0;
}

